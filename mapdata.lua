local function AppendRange(t, range)
    if ((2 == #range)
        and (type(range[1]) == "number")
        and (type(range[1]) == "number")
        and (range[2] > range[1]))
    then
        for i = range[1], range[2] do
            table.insert(t, i);
        end
    end
end

